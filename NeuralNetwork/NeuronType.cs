namespace NeuralNetwork
{
    /// <summary>
    /// Specifies the type of the neuron.
    /// </summary>
    public enum NeuronType
    {
        /// <summary>
        /// Represents the input neuron in the network.
        /// </summary>
        Input,

        /// <summary>
        /// Represents a regular (hidden) neuron.
        /// </summary>
        Normal,

        /// <summary>
        /// Represents the output neuron in the network.
        /// </summary>
        Output
    }
}
